R1 <- function(l1,l2,m){
  
  if(m==1){
    representer <- k1(l1)*k1(l2) +  k2(l1)*k2(l2) - k4( abs(l1-l2)  )
  }
  if(m==2){
    representer <- k2(l1)*k2(l2) - k4( abs(l1-l2) ) 
  }
  representer
}


